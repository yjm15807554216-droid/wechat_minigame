/**
 * 随机获取一个问题
 * @param {string} type - 'truth' 或 'dare'
 * @returns {string}
 */
function getRandomQuestion(type) {
  const bank = type === 'truth' ? truthQuestions : dareQuestions
  const index = Math.floor(Math.random() * bank.length)
  return bank[index]
}

